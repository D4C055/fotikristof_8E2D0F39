function kepElérésiÚt(kollekcioNev, elemNev) {
    const formazottKollekcio = kollekcioNev.replace(/ /g, '_');
    const formazottElem = elemNev.replace(/ /g, '_');
    return `kepek/${formazottKollekcio}/${formazottElem}`;
}
function kollekcioRendezes() {
    const kollekcioMegterulesek = Kollekciok.map(kollekcio => {
        const varhatoErtek = kollekcio.elemek.reduce((osszeg, elem) => {
            return osszeg + (elem.érték * elem.esély);
        }, 0);
        const kollekcioKoltseg = kollekcio.elemek[0].csillag * 0.35;
        const megterules = (varhatoErtek / kollekcioKoltseg) * 100;
        const legertékesebbElem = kollekcio.elemek.reduce((legjobb, elem) => {
            return elem.érték > legjobb.érték ? elem : legjobb;
        });
        
        return {
            név: kollekcio.név,
            megterules: megterules,
            varhatoErtek: varhatoErtek,
            koltseg: kollekcioKoltseg,
            legertékesebbElem: legertékesebbElem
        };
    });
    
    return kollekcioMegterulesek.sort((a, b) => b.megterules - a.megterules);
}

function renderKollekciok() {
    const rendezettKollekciok = kollekcioRendezes();
    const container = document.getElementById('kollekciokContainer');
    
    container.innerHTML = rendezettKollekciok.map(kollekcio => {
        let szinOsztaly = 'piros';
        if (kollekcio.megterules > 120) szinOsztaly = 'zold';
        else if (kollekcio.megterules > 100) szinOsztaly = 'szurke';
        
        if (kollekcio.megterules === rendezettKollekciok[0].megterules) {
            szinOsztaly = 'vilagoszold';
        }
        const kepUt = kepElérésiÚt(kollekcio.név, kollekcio.legertékesebbElem.név);
        
        return `
            <div class="kollekcio-doboz ${szinOsztaly}">
                <div class="kollekcio-kep">
                    <img src="${kepUt}.png" alt="${kollekcio.legertékesebbElem.név}" 
                         onerror="this.src='${kepUt}.jpg'; this.onerror=null;">
                </div>
                <div class="kollekcio-info">
                    <h3>${kollekcio.név.replace(/_/g, ' ')}</h3>
                    <p>Megtérülés: <strong>${kollekcio.megterules.toFixed(1)}%</strong></p>
                    <p>Várható érték: <strong>${kollekcio.varhatoErtek.toFixed(2)} EUR</strong></p>
                    <p>Költség: <strong>${kollekcio.koltseg.toFixed(2)} EUR</strong></p>
                    <p class="legjobb-elem">Legértékesebb: ${kollekcio.legertékesebbElem.név}</p>
                </div>
            </div>
        `;
    }).join('');
}
